import { useId } from 'react'

function FormField({ label }: { label: string }) {
  const id = useId()

  return (
    <div className="mb-4">
      <label htmlFor={id} className="block mb-2">
        {label}
      </label>
      <input
        id={id}
        type="text"
        className="w-full px-3 py-2 bg-gray-700 rounded text-white"
      />
    </div>
  )
}

export default function UseIdDemo() {
  const formId = useId()

  return (
    <div className="space-y-6">
      <h2 className="text-3xl font-bold">useId</h2>
      <p className="text-gray-400">Generates unique IDs for accessibility attributes</p>

      <div className="p-4 bg-gray-800 rounded-lg">
        <h3 className="text-xl mb-4">Form with Unique IDs</h3>
        <form id={formId}>
          <FormField label="Username" />
          <FormField label="Email" />
          <FormField label="Password" />
        </form>
        <p className="text-sm text-gray-500 mt-4">
          Each field has a unique ID generated by useId (inspect DOM)
        </p>
      </div>
    </div>
  )
}